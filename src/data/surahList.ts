// Global ayah start index for each chapter (1-indexed, chapter → first global ayah number)
export const CHAPTER_GLOBAL_START: number[] = (() => {
  const starts: number[] = [0]; // index 0 unused
  let cum = 0;
  for (const s of SURAH_LIST) {
    starts.push(cum + 1);
    cum += s.vc;
  }
  return starts;
})();

export function getGlobalAyah(chapter: number, verse: number): number {
  return CHAPTER_GLOBAL_START[chapter] + verse - 1;
}
